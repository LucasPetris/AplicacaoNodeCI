function soma(num1, num2) {
	return num1 + num2;
}

function subtracao(num1, num2) {
	return num1 - num2;
}

function multiplicao(num1, num2) {
	return num1 * num2;
}

function divisao(num1, num2) {
	return num1 / num2;
}

exports.multiplicao = multiplicao;
exports.divisao = divisao;
exports.soma = soma;
exports.subtracao = subtracao;